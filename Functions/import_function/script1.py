
def addition(x,y):
    print("-"*88)
    print(f"The addition of {x} and {y} is: {x + y}")
    print("-"*88)
    return None


def subtraction(x,y):
    print("-"*88)
    print(f"The subtraction of {x} and {y} is: {x - y}")
    print("-"*88)
    return None

def main():
    x = 10
    y = 45
    addition(x,y)  
    subtraction(x,y)
    return None

if __name__ == "__main__":
    main()


